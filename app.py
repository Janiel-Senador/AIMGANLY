def init_form_state(prefix: str) -> None:
    for field_name, default_value in CI_FORM_DEFAULTS.items():
        st.session_state.setdefault(f"{prefix}_{field_name}", default_value)


def seed_form_state(prefix: str, values: Dict[str, Any]) -> None:
    for field_name in CI_FORM_DEFAULTS:
        st.session_state[f"{prefix}_{field_name}"] = values.get(field_name, "")


def collect_form_data(prefix: str) -> Dict[str, Any]:
    return {field_name: st.session_state.get(f"{prefix}_{field_name}", "") for field_name in CI_FORM_DEFAULTS}


def clear_form_state(prefix: str) -> None:
    for field_name, default_value in CI_FORM_DEFAULTS.items():
        st.session_state[f"{prefix}_{field_name}"] = default_value


def init_session_state() -> None:
    st.session_state.setdefault("auth_user", None)
    st.session_state.setdefault("ci_submit_notes", "")
    st.session_state.setdefault("ci_clear_submit_notes", False)
    st.session_state.setdefault("ao_selected_record_id", "")
