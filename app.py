def render_ci_dashboard(user: Dict[str, str]) -> None:
    if st.session_state.get("ci_clear_submit_notes"):
        st.session_state["ci_submit_notes"] = ""
        st.session_state["ci_clear_submit_notes"] = False

    my_records = list_records_for_user("ci", user["email"])
    pending_count = sum(1 for record in my_records if record.get("status") == "submitted_to_ao")
    sent_count = sum(1 for record in my_records if record.get("status") == "submitted_to_officer")

    with st.sidebar:
        st.subheader("Session")
        st.write(f"Signed in as `{user['name']}`")
        st.write("Role: `CI`")
        st.divider()
        render_metric("My Notes", str(len(my_records)), "All notes submitted by this CI account.")
        render_metric("Waiting AO", str(pending_count), "Notes currently queued for AO review.")
        render_metric("Sent Forward", str(sent_count), "Records already delivered to the Officer inbox.")
        if st.button("Sign Out", use_container_width=True):
            logout()

    st.markdown(
        """
        <div class="hero">
            <h1>CI Notes Submission</h1>
            <p>
                CI accounts only submit notes into the shared inventory. AO will review, select the
                correct PDRN format, generate the workbook, and forward the finished file to the Officer.
            </p>
        </div>
        """,
        unsafe_allow_html=True,
    )

    tab_submit, tab_history = st.tabs(["Submit Notes", "My Submission History"])

    with tab_submit:
        st.text_area(
            "Paste CI notes here",
            key="ci_submit_notes",
            height=420,
            placeholder="Paste the full CI notes. AO will receive this in the inventory queue.",
        )
        if st.button("Submit Notes to AO Queue", type="primary", use_container_width=True):
            notes = st.session_state.get("ci_submit_notes", "").strip()
            if not notes:
                st.error("Please paste the CI notes before submitting.")
            else:
                record = create_ci_submission(notes, user)
                st.session_state["ci_clear_submit_notes"] = True
                st.success(f"Notes submitted successfully. Record ID: {record['id']}")
                st.rerun()

    with tab_history:
        if not my_records:
            st.info("No CI submissions yet.")
        for record in my_records:
            with st.expander(format_record_title(record)):
                render_record_metadata(record)
                st.markdown('<div class="section-title" style="margin-top:14px;">CI Notes</div>', unsafe_allow_html=True)
                st.markdown(f'<div class="note-box">{record.get("ci_notes", "")}</div>', unsafe_allow_html=True)


